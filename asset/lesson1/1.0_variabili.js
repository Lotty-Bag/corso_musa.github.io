 // LE VARIABILI

 // Una variabile è un nome simbolico per un valore che vogliamo "salvare" in memoria 
 // La variabile si può dichiarare con let o var

 var x; // Declare a variable named x
 // Ora la variabile vale x

 // i valori possono essere assegnati alle variabili tramite il simbolo = e un valore

 x = 0; // ora la variabile precedente vale 0 
 x // => se richiamo x il suo valore è consultato (ovvero 0)

 // I valori in js possono essere

 x = 1; // numeri
 x = 0.01; // numeri interi o reali
 x = "Hello world" // stringhe di testo tra virgolette
 x = 'Javascript' // stringhe di testo con quote marks
 x = true; // Valore booleano
 x = false // Valore booleano
 x = null; // Null è un VALORE speciale che significa che non ha valore
 x = undefined; // Undefined significa che non ha MAI avuto valore          